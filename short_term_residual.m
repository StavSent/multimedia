function d = short_term_residual(s, coeff)
    d = zeros(length(s), 1);
    d(1:13) = [s(1:13), [zeros(1, 1); s(1:12)], [zeros(2, 1); s(1:11)], [zeros(3, 1); s(1:10)],...
              [zeros(4, 1); s(1:9)], [zeros(5, 1); s(1:8)],...
              [zeros(6, 1); s(1:7)], [zeros(7, 1); s(1:6)],...
              [zeros(8, 1); s(1:5)]] * coeff(:, 1);
    d(14:27) = [s(14:27), s(13:26), s(12:25), s(11:24), s(10:23),...
               s(9:22), s(8:21), s(7:20), s(6:19)] * coeff(:, 2);
    d(28:40) = [s(28:40), s(27:39), s(26:38), s(25:37), s(24:36),... 
               s(23:35), s(22:34), s(21:33), s(20:32)] * coeff(:, 3);
    d(48:160) = [s(48:160), s(47:159), s(46:158), s(45:157), s(44:156),... 
               s(43:155), s(42:154), s(41:153), s(40:152)] * coeff(:, 4);
end